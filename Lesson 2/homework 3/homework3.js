const userNum1 = Number(prompt('Введите первое число:'));
const userNum2 = Number(prompt('Введите второе число:'));

alert(`
       ${userNum1} + ${userNum2} = ${userNum1 + userNum2}\n
       ${userNum1} - ${userNum2} = ${userNum1 - userNum2}\n
       ${userNum1} * ${userNum2} = ${userNum1 * userNum2}\n
       ${userNum1} / ${userNum2} = ${userNum1 / userNum2}`)